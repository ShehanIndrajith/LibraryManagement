    private void Button_BookIssuedActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Button_BookIssuedActionPerformed
        String bookidtxt = TextField_BookID.getText();
    String studentidtxt = TextField_StudentID1.getText();
    String ReturnDate = TextField_ReturnDate.getText();
    boolean textFeildEmpty = false;

    // Validate inputs
    if (bookidtxt.equals("")) {
        Label_TopicBookIssueError.setVisible(true);
        Label_TopicBookIssueError.setText("Please Enter the Book ID");
        textFeildEmpty = true;
    }
    if (studentidtxt.equals("")) {
        Label_StudentIDError.setVisible(true);
        Label_StudentIDError.setText("Please Enter the Student ID");
        textFeildEmpty = true;
    }

    if (!textFeildEmpty) {
        int bookid = Integer.parseInt(bookidtxt);
        int studentid = Integer.parseInt(studentidtxt);

        try {
            DBConnector dbc = new DBConnector();
            int status = dbc.IssueBook(bookid, studentid, ReturnDate);
            if (status > 0) {
                JOptionPane.showMessageDialog(null, "Book Issued Successfully.", "Success", JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(null, "Issue Failed. Please Try Again.", "Error", JOptionPane.ERROR_MESSAGE);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
    }//GEN-LAST:event_Button_BookIssuedActionPerformed

