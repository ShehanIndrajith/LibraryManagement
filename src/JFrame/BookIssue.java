    public void BookIssueTable() {
        try {
            DBConnector dbc = new DBConnector();

            Object[][] data = dbc.BookIssueTable();
            int rows = data.length;

            DefaultTableModel tableModel = (DefaultTableModel) Table_bookIssue.getModel();
            for (Object[] row : data) {
                tableModel.addRow(row);
            }
            DefaultTableCellRenderer leftRenderer = new DefaultTableCellRenderer();
            leftRenderer.setHorizontalAlignment(SwingConstants.LEFT);

            for (int i = 0; i < Table_bookIssue.getColumnCount(); i++) {
                Table_bookIssue.getColumnModel().getColumn(i).setCellRenderer(leftRenderer);
            }
            Table_bookIssue.setDefaultEditor(Object.class, null);

        } catch (SQLException ex) {
            Logger.getLogger(BookIssue.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

