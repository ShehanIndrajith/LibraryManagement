    public BookDetailsChange1(int bookID) {
        initComponents();
        this.bookID = bookID;

        try {
        DBConnector dbConnector = new DBConnector();
        Books book = dbConnector.getBookDetails(bookID);
        
        if (book != null) {
            String BookID = String.valueOf(book.getBookID());
            TextField_BoookIDChange.setText(BookID);
            TextField_BoookNameChange.setText(book.getBookName());
            TextField_AuotherChange.setText(book.getAuthor());
            TextField_DateChange.setText(book.getDate());
            String quantity = String.valueOf(book.getQuantity());
            TextField_QuantityChange.setText(quantity);
            
        } else {
            JOptionPane.showMessageDialog(null, "No student found with the provided ID.");
        }
    } catch (SQLException e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(null, "Error fetching student details: " + e.getMessage());
    }

    }
    
