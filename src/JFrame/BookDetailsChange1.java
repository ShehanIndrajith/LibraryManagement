    public void bookDeatails(String returnstring) {

        String[] bookDeatails = returnstring.split("\\|");

        String splitBookId = bookDeatails[0];
        String splitName = bookDeatails[1];
        String splitAuthor = bookDeatails[2];
        String splitDate = bookDeatails[3];
        String splitQuantity = bookDeatails[4];

        TextField_BoookIDChange.setText(splitBookId);
        TextField_BoookNameChange.setText(splitName);
        TextField_AuotherChange.setText(splitAuthor);
        TextField_DateChange.setText(splitDate);
        TextField_QuantityChange.setText(splitQuantity);

        TextField_BoookIDChange.setEditable(false);
    }


    private void Button_BookDetailsChangeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Button_BookDetailsChangeActionPerformed
        String bookname = TextField_BoookNameChange.getText();
        String auother = TextField_AuotherChange.getText();
        String date = TextField_DateChange.getText();
        String qua = TextField_QuantityChange.getText();
        int quantity = Integer.parseInt(TextField_QuantityChange.getText());
        String bookid = TextField_BoookIDChange.getText();
        int intbookid = Integer.parseInt(TextField_BoookIDChange.getText());

        boolean textfeildEmty = false;

        if (bookname.equals("")) {
            Label_BookNameError1.setVisible(true);
            Label_BookNameError1.setText("Please enter book name");
            textfeildEmty = true;
        }
        if (auother.equals("")) {
            Label_AuotherError1.setVisible(true);
            Label_AuotherError1.setText("Please Eneter Auother name");
            textfeildEmty = true;
        }
        if (date.equals("")) {
            Label_DateError1.setVisible(true);
            Label_DateError1.setText("Please Enter Date");
            textfeildEmty = true;
        }
        if (qua.equals("")) {
            Label_quantityError1.setVisible(true);
            Label_quantityError1.setText("Please Entere the Quantity");
            textfeildEmty = true;
        }

        if (!textfeildEmty) {
            try {
                DBConnector dbc = new DBConnector();
                int rs = dbc.EditBookDetails(intbookid, bookname, auother, date, quantity);

                if (rs > 0) {
                    JOptionPane.showMessageDialog(null, "Change Succesfull.", "Success", JOptionPane.INFORMATION_MESSAGE);

                } else {
                    JOptionPane.showMessageDialog(null, "PLease try again.", "Error", JOptionPane.ERROR_MESSAGE);
                }

            } catch (SQLException e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_Button_BookDetailsChangeActionPerformed

